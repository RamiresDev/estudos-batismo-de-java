package DesafiosCodeWars;

public class DesafioStringToNumber {
    public static int stringToNumber(String str) {
       /*
       * Precisamos de uma função que transforme uma string em um número. Que maneiras você conhece de fazer isso?
       * Observação: não se preocupe, todas as entradas serão strings e cada string é uma representação perfeitamente válida de um número integral.
       * */

        return Integer.parseInt(str);

    }
}
